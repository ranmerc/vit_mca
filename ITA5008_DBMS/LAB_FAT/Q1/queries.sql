-- a.
COLUMN NAME FORMAT A10;

COLUMN ADDRESS FORMAT A40;

SELECT
    NAME,
    ADDRESS
FROM
    GUEST
WHERE
    LOWER(ADDRESS) LIKE ('%hyderabad%')
ORDER BY
    NAME;

-- b.
SELECT
    AVG(PRICE)
FROM
    ROOM;

-- c.
SELECT
    *
FROM
    ROOM
WHERE
    TYPE IN ('DOUBLE', 'FAMILY')
    AND PRICE < 40
ORDER BY
    PRICE;

-- d.
SELECT
    GUEST.NAME
FROM
    GUEST,
    ROOM,
    HOTEL
WHERE
    GUEST.ROOM_NO = ROOM.ROOM_NO
    AND ROOM.HOTEL_NO = HOTEL.HOTEL_NO
    AND HOTEL.NAME LIKE('PARADISE');

-- e.
